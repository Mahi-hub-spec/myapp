let dayNumber = prompt("Enter a number between 1 and 7:");
// Promt() is used for taking a value from user 
if (dayNumber == 1) {
  alert("It's Monday"); //alert is used for showing a message on a screen 
} else if (dayNumber == 2) {
  alert("It's Tuesday");
} else if (dayNumber == 3) {
  alert("It's Wednesday");
} else if (dayNumber == 4) {
  alert("It's Thursday");
} else if (dayNumber == 5) {
  alert("It's Friday");
} else if (dayNumber == 6) {
  alert("It's Saturday");
} else if (dayNumber == 7) {
  alert("It's Sunday");
} else {
  alert("This Day Come on coming week, Please enter a number between 1 and 7.");
}
